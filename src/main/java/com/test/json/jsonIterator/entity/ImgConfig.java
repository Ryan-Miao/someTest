public class ImgConfig {
    private String artworkX;
    private String artworkY;
    private String hotelNameLTX;
    private String hotelNameLTY;
    private String hotelNameRBX;
    private String hotelNameRBY;
    private String hotelNameFontSize;
    private String hotelNameFontFamily;
    private String hotelNameFontColor;

    public ImgConfig() {
    }

    public ImgConfig(String artworkX, String artworkY, String hotelNameLTX, String hotelNameLTY, String hotelNameRBX, String hotelNameRBY, String hotelNameFontSize, String hotelNameFontFamily, String hotelNameFontColor) {
        this.artworkX = artworkX;
        this.artworkY = artworkY;
        this.hotelNameLTX = hotelNameLTX;
        this.hotelNameLTY = hotelNameLTY;
        this.hotelNameRBX = hotelNameRBX;
        this.hotelNameRBY = hotelNameRBY;
        this.hotelNameFontSize = hotelNameFontSize;
        this.hotelNameFontFamily = hotelNameFontFamily;
        this.hotelNameFontColor = hotelNameFontColor;
    }

    public String getArtworkX() {
        return artworkX;
    }

    public void setArtworkX(String artworkX) {
        this.artworkX = artworkX;
    }

    public String getArtworkY() {
        return artworkY;
    }

    public void setArtworkY(String artworkY) {
        this.artworkY = artworkY;
    }

    public String getHotelNameLTX() {
        return hotelNameLTX;
    }

    public void setHotelNameLTX(String hotelNameLTX) {
        this.hotelNameLTX = hotelNameLTX;
    }

    public String getHotelNameLTY() {
        return hotelNameLTY;
    }

    public void setHotelNameLTY(String hotelNameLTY) {
        this.hotelNameLTY = hotelNameLTY;
    }

    public String getHotelNameRBX() {
        return hotelNameRBX;
    }

    public void setHotelNameRBX(String hotelNameRBX) {
        this.hotelNameRBX = hotelNameRBX;
    }

    public String getHotelNameRBY() {
        return hotelNameRBY;
    }

    public void setHotelNameRBY(String hotelNameRBY) {
        this.hotelNameRBY = hotelNameRBY;
    }

    public String getHotelNameFontSize() {
        return hotelNameFontSize;
    }

    public void setHotelNameFontSize(String hotelNameFontSize) {
        this.hotelNameFontSize = hotelNameFontSize;
    }

    public String getHotelNameFontFamily() {
        return hotelNameFontFamily;
    }

    public void setHotelNameFontFamily(String hotelNameFontFamily) {
        this.hotelNameFontFamily = hotelNameFontFamily;
    }

    public String getHotelNameFontColor() {
        return hotelNameFontColor;
    }

    public void setHotelNameFontColor(String hotelNameFontColor) {
        this.hotelNameFontColor = hotelNameFontColor;
    }
}
